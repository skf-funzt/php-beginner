<?php
print("<pre>");
// Comparison operators in PHP
$a = 10;
$b = 5;

// Equal to
$result = ($a == $b);
echo "Equal to: ";
var_dump($result);

// Not equal to
$result = ($a != $b);
echo "Not equal to: ";
var_dump($result);

// Greater than
$result = ($a > $b);
echo "Greater than: ";
var_dump($result);

// Less than
$result = ($a < $b);
echo "Less than: ";
var_dump($result);

// Greater than or equal to
$result = ($a >= $b);
echo "Greater than or equal to: ";
var_dump($result);

print("</pre>");
?>
